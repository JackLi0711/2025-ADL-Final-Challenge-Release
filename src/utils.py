for ch in list("abcdefghijklmnopqrstuvwxyz"):
    ASCII_FONT_BIG[ch] = ASCII_FONT_BIG[ch.upper()]

def ascii_char_unknown(ch):
    return ASCII_FONT_BIG.get("_UNKNOWN_")

def word_to_ascii_big(word):
    # assumes font height of 6 rows
    rows = [""] * 6
    for ch in word.upper():
        art = ASCII_FONT_BIG.get(ch, ascii_char_unknown(ch))
        for i in range(6):
            rows[i] += art[i] + "  "
    return "\n".join(rows)


